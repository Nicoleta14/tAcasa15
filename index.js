//Creati o functie care va aduna 2 cifre si va intoarce suma.
export function suma(a, b) {
    return a + b;
  }
  
// ex 2------------------------- 
// Creati o functie care intoare un string concatinat din last name
// si first name pe care le primeste ca argumente.

export function concatinare(lastName, firstName) {
    return lastName + ' ' + firstName;
  }
  
